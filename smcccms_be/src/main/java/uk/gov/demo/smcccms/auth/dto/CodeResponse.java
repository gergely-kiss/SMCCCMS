package uk.gov.demo.smcccms.auth.dto;

public class CodeResponse {
    
    private String code;
    
    public CodeResponse() {}
    
    public CodeResponse(String code) {
        this.code = code;
    }
    
    public String getCode() {
        return code;
    }
    
    public void setCode(String code) {
        this.code = code;
    }
}